import type { WorkflowModule } from '../types/workflow';

/** Label on the edge leading INTO this stage (from previous). */
export interface WorkflowStage extends WorkflowModule {
  emoji: string;
  edgeFromPrevious: string;
  dataBefore: string[];
  dataAfter: string[];
  operations: string[];
}

function stage(
  partial: Omit<WorkflowStage, 'completionPercentage' | 'color' | 'relationships' | 'apiEndpoints' | 'screens' | 'validationRules' | 'notifications' | 'reports' | 'auditLogs' | 'attachments' | 'futureImprovements' | 'developmentNotes' | 'businessRules' | 'estimatedHours' | 'complexity' | 'features' | 'businessFlow'> &
    Partial<WorkflowStage>
): WorkflowStage {
  return {
    completionPercentage: 0,
    color: '#D32F2F',
    relationships: [],
    apiEndpoints: [],
    screens: [],
    validationRules: [],
    notifications: [],
    reports: [],
    auditLogs: [],
    attachments: [],
    futureImprovements: [],
    developmentNotes: '',
    businessRules: [],
    estimatedHours: 24,
    complexity: 'medium',
    features: [],
    businessFlow: [],
    ...partial,
  };
}

